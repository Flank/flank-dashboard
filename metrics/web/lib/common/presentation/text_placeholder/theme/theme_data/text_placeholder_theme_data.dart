import 'package:flutter/material.dart';

/// A class that stores the theme data for the text placeholders.
class TextPlaceholderThemeData {
  /// The [TextStyle] of the text in the text placeholder.
  final TextStyle textStyle;

  /// Create a new instance of the [TextPlaceholderThemeData].
  const TextPlaceholderThemeData({this.textStyle});
}
